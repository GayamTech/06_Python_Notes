# Random Number

# Python does not have a random() function to make a random number, but Python has a
# built-in module called random that can be used to make random numbers:


# Import the random module, and display a random number between 1 and 9

import random

print("")
print("Importing the random module allows you to display a random number")
print("")
print("random.randrange(1, 10) : display a random number between 1 and 9")
print("")
print(random.randrange(1, 10))
print("")
